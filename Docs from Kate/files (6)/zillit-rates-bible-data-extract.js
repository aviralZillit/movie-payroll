#!/usr/bin/env node
/**
 * ─────────────────────────────────────────────────────────────────────────────
 * Zillit Global Rates Bible — Data Extraction Script
 * 
 * Extracts the BIBLE data array from the standalone HTML prototype and outputs
 * typed TypeScript files ready for import into the React application.
 *
 * Usage:
 *   node data-extract.js <path-to-html> [--out <output-dir>]
 *
 * Outputs:
 *   ratesData.ts      — BIBLE array exported as const Territory[]
 *   aiSystemPrompt.ts — Compressed rates context for the AI assistant
 *
 * Example:
 *   node data-extract.js zillit-global-rates-bible-v10.html --out ./src/data
 * ─────────────────────────────────────────────────────────────────────────────
 */

const fs   = require('fs')
const path = require('path')

// ── ARGUMENTS ─────────────────────────────────────────────────────────────────

const args    = process.argv.slice(2)
const htmlArg = args.find(a => !a.startsWith('--'))
const outIdx  = args.indexOf('--out')
const outDir  = outIdx >= 0 ? args[outIdx + 1] : './src/data'

if (!htmlArg) {
  console.error('Usage: node data-extract.js <html-file> [--out <dir>]')
  process.exit(1)
}

const htmlPath = path.resolve(htmlArg)
if (!fs.existsSync(htmlPath)) {
  console.error(`File not found: ${htmlPath}`)
  process.exit(1)
}

// ── EXTRACT BIBLE ARRAY ───────────────────────────────────────────────────────

const html = fs.readFileSync(htmlPath, 'utf8')

// Find BIBLE = [ ... ] // end BIBLE
const match = html.match(/var BIBLE\s*=\s*(\[[\s\S]*?\])\s*\/\/\s*end BIBLE/)
if (!match) {
  console.error('Could not find BIBLE array in HTML. Check the marker "// end BIBLE" exists.')
  process.exit(1)
}

const bibleJs = match[1]

// Evaluate the BIBLE array in a sandboxed context
let bible
try {
  // Use Function constructor to evaluate the JS object literal safely
  bible = new Function(`return ${bibleJs}`)()
  console.log(`✓ Parsed ${bible.length} territories`)
} catch (e) {
  console.error('Failed to parse BIBLE array:', e.message)
  process.exit(1)
}

// ── VALIDATE STRUCTURE ────────────────────────────────────────────────────────

const requiredKeys = ['id', 'flag', 'name', 'region', 'currency', 'tz', 'summary', 'agreements', 'rules', 'incentives']
let valid = true
bible.forEach((t, i) => {
  requiredKeys.forEach(k => {
    if (!(k in t)) {
      console.warn(`Territory[${i}] (${t.id || '?'}) missing key: ${k}`)
      valid = false
    }
  })
})

if (valid) {
  const totalConf = bible.reduce((s, t) =>
    s + t.agreements.filter(a => a.status === 'confirmed').length, 0)
  console.log(`✓ Structure valid — ${totalConf} confirmed agreements across ${bible.length} territories`)
}

// ── OUTPUT: ratesData.ts ──────────────────────────────────────────────────────

if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true })

const ratesDataTs = `// AUTO-GENERATED by data-extract.js — DO NOT EDIT MANUALLY
// Re-run: node data-extract.js <html-file> --out ${outDir}
// Source: ${path.basename(htmlPath)}
// Generated: ${new Date().toISOString()}

import type { Territory } from '../components/rates-bible/types'

export const BIBLE: Territory[] = ${JSON.stringify(bible, null, 2)} as const
`

const ratesDataPath = path.join(outDir, 'ratesData.ts')
fs.writeFileSync(ratesDataPath, ratesDataTs)
console.log(`✓ Wrote ${ratesDataPath} (${Math.round(ratesDataTs.length / 1024)}KB)`)

// ── OUTPUT: aiSystemPrompt.ts ─────────────────────────────────────────────────

// Build a compressed text representation of all rates for the AI context window.
// We prioritise: confirmed rates, working rules, key penalties.

function compressForAI(bible) {
  const lines = []

  bible.forEach(t => {
    lines.push(`\n=== ${t.flag} ${t.name} (${t.currency}) ===`)
    lines.push(t.summary)

    // Confirmed agreements only
    const confirmed = t.agreements.filter(a => a.status === 'confirmed')
    if (confirmed.length) {
      confirmed.forEach(a => {
        lines.push(`\n[${a.name}] (${a.eff})${a.holNote ? ' ⚠ ' + a.holNote : ''}`)
        a.rows.forEach(r => {
          if (r.isHd) lines.push(`  — ${r.hd}`)
          else if (r.g) lines.push(`  ${r.g}: ${r.r1 || '—'}${r.r2 ? ' / ' + r.r2 : ''}`)
        })
      })
    }

    // Rules (all, flagging warn items)
    if (t.rules && t.rules.length) {
      t.rules.forEach(rg => {
        if (!rg || !rg.items) return
        lines.push(`\n[Rules: ${rg.union}]`)
        rg.items.forEach(ri => {
          if (!ri) return
          lines.push(`  ${ri.warn ? '⚠ ' : ''}${ri.k}: ${ri.v}`)
        })
      })
    }

    // Incentives
    if (t.incentives.length) {
      lines.push('\n[Incentives]')
      t.incentives.forEach(inc => {
        lines.push(`  ${inc.name}: ${inc.rate}`)
        inc.items.forEach(i => lines.push(`    ${i.k}: ${i.v}`))
      })
    }
  })

  return lines.join('\n')
}

const ratesContext = compressForAI(bible)

const systemPrompt = `You are a specialist production accounting and payroll assistant for the Zillit Global Production Rates Bible.

You have confirmed rate data loaded below. Use it to answer deal memo questions, timecard calculations, OT queries, and compliance questions.

RULES FOR RESPONSES:
- Always quote exact figures from the data below, not approximations
- For UK rates, always state which branch and budget tier
- Always clarify holiday pay treatment (included/excluded/separate) when quoting UK rates
- For OT calculations, show the formula clearly (e.g. weekly rate ÷ 55 = hourly, then × 2 for Camera OT)
- If a rate is Ind. Neg. or Own Negotiation, say so explicitly — never invent a figure
- Items marked ⚠ in the rules are the most common sources of unbudgeted cost — highlight these
- Be concise and deal-memo ready

--- RATE DATA ---
${ratesContext}
--- END DATA ---`

const aiPromptTs = `// AUTO-GENERATED by data-extract.js — DO NOT EDIT MANUALLY
// Re-run: node data-extract.js <html-file> --out ${outDir}
// Generated: ${new Date().toISOString()}

export const AI_SYSTEM_PROMPT = ${JSON.stringify(systemPrompt)}
`

const aiPromptPath = path.join(outDir, 'aiSystemPrompt.ts')
fs.writeFileSync(aiPromptPath, aiPromptTs)
console.log(`✓ Wrote ${aiPromptPath} (${Math.round(aiPromptTs.length / 1024)}KB)`)

console.log('\n✓ Done. Import in your components:')
console.log(`  import { BIBLE } from '@/data/ratesData'`)
console.log(`  import { AI_SYSTEM_PROMPT } from '@/data/aiSystemPrompt'`)
